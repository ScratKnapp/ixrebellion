ITEM.functions._use = {
    name = "Use",
    icon = "icon16/pill.png",
    OnRun = function(item)
        local client = item.player
        local char = client:GetCharacter()
        if char and client:Alive() then
            char:AddFeat(item.feat)
        end
    end,
    OnCanRun = function(item) return not IsValid(item.entity) end
